function normalizeLocale(value) {
  return String(value || "").toLowerCase().startsWith("en") ? "en" : "zh-CN";
}

function currentLocale() {
  return normalizeLocale(localStorage.getItem("gmind.locale") || navigator.language);
}

function translate(key, vars = {}) {
  const locale = currentLocale();
  const template = GMIND_LOCALES[locale]?.[key] ?? GMIND_LOCALES["zh-CN"][key] ?? key;
  return Object.entries(vars).reduce(
    (text, [name, value]) => text.replaceAll(`{${name}}`, String(value)),
    template,
  );
}

function applyTranslations(root = document) {
  document.documentElement.lang = currentLocale();
  root.querySelectorAll("[data-i18n]").forEach((node) => {
    node.textContent = translate(node.dataset.i18n);
  });
  root.querySelectorAll("[data-i18n-placeholder]").forEach((node) => {
    node.placeholder = translate(node.dataset.i18nPlaceholder);
  });
  root.querySelectorAll("[data-i18n-aria]").forEach((node) => {
    node.setAttribute("aria-label", translate(node.dataset.i18nAria));
  });
}

function setLocale(locale) {
  localStorage.setItem("gmind.locale", normalizeLocale(locale));
  applyTranslations();
  window.dispatchEvent(new CustomEvent("gmind:locale-change"));
}

window.GMindI18n = {
  apply: applyTranslations,
  locale: currentLocale,
  setLocale,
  t: translate,
};
